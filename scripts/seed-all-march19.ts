async function main() {
  console.log("🚀 สร้าง/อัปเดต 7 โพสต์วันที่ 19 มีนาคม 2026...\n");

  let created = 0;
  let updated = 0;

  for (const post of posts) {
    try {
      const result = await prisma.post.upsert({
        where: { slug: post.slug },
        update: { coverImage: post.coverImage },
        create: { ...post, content: "" },
      });
      const existed = result.createdAt.getTime() !== result.updatedAt.getTime();
      if (existed) {
        updated++;
        console.log(`🔄 Updated: ${post.slug}`);
      } else {
        created++;
        console.log(`✅ Created: ${post.slug}`);
      }
      console.log(`   coverImage: ${result.coverImage}`);
    } catch (err: any) {
      console.error(`❌ ${post.slug}: ${err.message}`);
    }
  }

  console.log(`\n📊 สรุป: สร้างใหม่ ${created} | อัปเดต ${updated} | รวม ${created + updated}/7`);
  console.log("🎉 เสร็จสิ้น!");
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
